public class AtribuicaoSimples {
    public static void main(String[] args) {
        /*
         * Enunciado: Crie um programa que declare uma variável inteira, atribua um
         * valor a ela e,
         * em seguida, modifique o valor utilizando os operadores de atribuição (+=, -=,
         * *=, /=, %=).
         * Exiba o resultado após cada operação.
         */

        int n = 10;
        System.out.println(n += 2);
        System.out.println(n -= 2);
        System.out.println(n *= 2);
        System.out.println(n /= 2);
        System.out.println(n %= 2);
        
    }
}
