package exe;

public class OperadoresAritmeticos {
    public static void main(String[] args) {

        /*
         * Exercício 5: Operações Aritméticas
         * Desenvolva um programa que declare duas variáveis int e realize as operações
         * de soma,
         * subtração, multiplicação, divisão e módulo entre elas. Exiba os resultados de
         * cada
         * operação.
         */

        int x = 10;
        int y = 2;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
    }
}
